sub_res = num1 - num2
mul_res = num1 * num2
div_res = num1 / num2
int_div_res = num1 // num2

print(f"The addition of both numbers is {add_res}")
print(f"The subtraction of both numbers is {sub_res}")
print(f"The multiplication of both numbers is {mul_res}")
print(f"The division of both numbers is {div_res}")
print(f"The integer division of both numbers is {int_div_res}")


